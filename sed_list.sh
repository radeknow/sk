hex_to_char () {
  printf "\\U$(printf '%08x' "$((16#$1))")"
}

for LINIA in "${ZAMIANY[@]}"
do
  [[ -z "$LINIA" ]] && continue

  LEFT=${LINIA%%|*}
  RIGHT=${LINIA#*|}

  HEX1=$(echo "$LEFT" | sed -E 's/chr\(0x([0-9A-Fa-f]+)\)/\1/')
  HEX2=$(echo "$RIGHT" | sed -E 's/chr\(0x([0-9A-Fa-f]+)\)/\1/')

  CHAR1=$(hex_to_char "$HEX1")
  CHAR2=$(hex_to_char "$HEX2")

  OLD_ESC=$(printf '%s\n' "$CHAR1" | sed 's/[\/&]/\\&/g')
  NEW_ESC=$(printf '%s\n' "$CHAR2" | sed 's/[\/&]/\\&/g')

  echo "s|$OLD_ESC|$NEW_ESC|g" >> "$SED_SCRIPT"
done

sed -f "$SED_SCRIPT" "$INPUT" > "$OUTPUT"

rm "$SED_SCRIPT"

